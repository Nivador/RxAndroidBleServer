package com.nexenio.rxandroidbleserver.exception;

public class RxBleServerException extends Exception {

    public RxBleServerException() {
        super();
    }

    public RxBleServerException(String message) {
        super(message);
    }

    public RxBleServerException(String message, Throwable cause) {
        super(message, cause);
    }

    public RxBleServerException(Throwable cause) {
        super(cause);
    }

}
